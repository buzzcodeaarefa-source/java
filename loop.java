class loop {
    public static void main(String[] args) {

        for (int w = 10; w > 1; w--) {
            System.out.println("The value of w is: " + w);
        }

        int z = 0;
        while (z < 11) {
            System.out.println("The value of z is: " + z);
            z++;
        }
    }
}